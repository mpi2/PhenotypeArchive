package uk.ac.ebi.phenotype.pojo;

/**
 * 
 * Some observation are meta data associated to a parameter or 
 * procedure and provides information on the context of the 
 * experiment (device, equipment, experimental conditions).
 * 
 * @author Gautier Koscielny (EMBL-EBI) <koscieln@ebi.ac.uk>
 *  @since February 2012
 *  @see Observation
 */

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.PrimaryKeyJoinColumn;
import javax.persistence.Table;

@Entity
@PrimaryKeyJoinColumn(name="id")
@Table(name = "metadata_observation")
public class MetaDataObservation extends Observation {

	@Column(name = "property_value")
	private String value;

	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}

	/**
	 * @param value the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
